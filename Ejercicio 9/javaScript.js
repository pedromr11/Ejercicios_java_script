
//Función con bucle while
function numeros(){
    var i = 1;
    while(i <= 35){
        if(i%2 != 0){
            console.log(i);
        }
        i++;
    }
}

//Función con bucle for
function numeros2(){
    for (let i = 1; i <= 35; i++){
        if(i%2 != 0){
            console.log(i);
        }
    }
}